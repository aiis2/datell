/**
 * Build flag generator — runs BEFORE tsc/vite to inject compile-time constants.
 *
 * When ENTERPRISE_BUILD=true is set in the environment, this script writes
 * `src/main/buildFlags.ts` with `export const ENTERPRISE_BUILD = true`,
 * which gets compiled into dist-electron/buildFlags.js. Vite uses the same
 * env var to define `__ENTERPRISE_BUILD__` for renderer code.
 *
 * Usage:
 *   node scripts/write-build-flags.js          # community/OSS build
 *   ENTERPRISE_BUILD=true node scripts/write-build-flags.js  # enterprise build
 */
'use strict';

const fs = require('fs');
const path = require('path');

const enterpriseBuild = process.env.ENTERPRISE_BUILD === 'true';
const content = `// AUTO-GENERATED by scripts/write-build-flags.js — DO NOT EDIT\n// ${enterpriseBuild ? 'ENTERPRISE' : 'Community/OSS'} build — generated at ${new Date().toISOString()}\nexport const ENTERPRISE_BUILD = ${enterpriseBuild};\n`;

const outPath = path.join(__dirname, '../src/main/buildFlags.ts');
fs.writeFileSync(outPath, content, 'utf-8');
console.log('[write-build-flags] ENTERPRISE_BUILD =', enterpriseBuild);
